    public void calcolaIntersezioni(){
        arrayintx.clear();
        arrayinty.clear();
        double d,dx,dy,x,y;
        for(int i=0;i<array.size();i++){
            for(int j=0;j<array.size();j++){
                d = (array.get(i).par_X*array.get(j).par_Y)-(array.get(i).par_Y*array.get(j).par_X);
                if(d!=0){
                    dx = (array.get(i).val*array.get(j).par_Y)-(array.get(i).par_Y*array.get(j).val);
                    dy = (array.get(i).par_X*array.get(j).val)-(array.get(i).val*array.get(j).par_X);
                    x = dx/d;
                    y = dy/d;
                    System.out.println("intersezione: "+x+" "+y);
                    arrayintx.add(x);
                    arrayinty.add(y);
                    d=0;
                }
                
            }
        }
    }
    
    public void trovaAreaAmmissibile(){
        for(int i=0;i<array.size();i++){
            if(array.get(i).operatore == minore){
                if((array.get(i).par_X*array.get(i).intersezioneX0)+(array.get(i).par_Y*array.get(i).intersezioneY0) <= array.get(i).val){
                    areaammx.add(array.get(i).intersezioneX0);
                    areaammy.add(array.get(i).intersezioneY0);
                    System.out.println("punto ammissibile :"+array.get(i).intersezioneX0+" "+array.get(i).intersezioneY0);
                }
            }
            if(array.get(i).operatore == maggiore){
                if((array.get(i).par_X*array.get(i).intersezioneX0)+(array.get(i).par_Y*array.get(i).intersezioneY0) >= array.get(i).val){
                    areaammx.add(array.get(i).intersezioneX0);
                    areaammy.add(array.get(i).intersezioneY0);
                    System.out.println("punto ammissibile :"+array.get(i).intersezioneX0+" "+array.get(i).intersezioneY0);
                }
            }
        }
    }

